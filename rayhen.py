import pygame
from pygame.locals import *
pygame.init

##-------------------------------------//Variables//-------------------------------------##
x = 0
xspeed = 5
y = 260
yspeed = 5

delay = 50

##-------------------------------------//Creating Window//-------------------------------------##

####--------------------------------------------------------------------------------------------------------------------------------------------------------------------####
####------------------------------------------------------------------/////DO NOT ALTER THE BELOW/////------------------------------------------------------------------####
window = pygame.display.set_mode((960,540))


running = True
while running == True:
    pygame.time.delay(delay)
    # This will repeat


    # checks if x button is pressed to quit the game
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
####------------------------------------------------------------------/////DO NOT ALTER THE ABOVE/////------------------------------------------------------------------####
####--------------------------------------------------------------------------------------------------------------------------------------------------------------------#### 
            
##-------------------------------------//Creating Objects//-------------------------------------##
    
#--------------------------------/The Ball/--------------------------------#
    window.fill((0,0,0))
    pygame.draw.rect(window, (255,255,255), (x,y,20,20))
    x = x + xspeed
    y = y + yspeed

    if (x > 940):
        xspeed = -xspeed
    if (0 > x):
        xspeed = -xspeed
    if (y > 520):
        yspeed = -yspeed
    if (0 > y):
        yspeed = -yspeed

##-------------------------------------//Keybinds//-------------------------------------##

    keys = pygame.key.get_pressed()
    if keys[K_UP]:
        delay = delay + 5
    keys = pygame.key.get_pressed()
    if keys[K_DOWN]:
        delay = delay - 5

####----------------------------------------------------------------------/////END OF SCRIPT/////----------------------------------------------------------------------####
        
    pygame.display.update() #updates screen
pygame.quit()
